package org.openmrs.module.chartsearch;

public class NonPatientToBeIndexedProject {
	
	private String projectName;
	
	private String projectDesc;
	
	private String mysqlQuery;
	
	private String columns;
	
	private String databaseName;
	
	private String databaseUser;
	
	private String databaseUserPassword;
	
	private String databaseServer;
	
	private String databaseManager;
	
	private String databasePortNumber;
	
	public String getProjectName() {
		return projectName;
	}
	
	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}
	
	public String getProjectDesc() {
		return projectDesc;
	}
	
	public void setProjectDesc(String projectDesc) {
		this.projectDesc = projectDesc;
	}
	
	public String getMysqlQuery() {
		return mysqlQuery;
	}
	
	public void setMysqlQuery(String mysqlQuery) {
		this.mysqlQuery = mysqlQuery;
	}
	
	public String getColumns() {
		return columns;
	}
	
	public void setColumns(String columns) {
		this.columns = columns;
	}
	
	public String getDatabaseName() {
		return databaseName;
	}
	
	public void setDatabaseName(String databaseName) {
		this.databaseName = databaseName;
	}
	
	public String getDatabaseUser() {
		return databaseUser;
	}
	
	public void setDatabaseUser(String databaseUser) {
		this.databaseUser = databaseUser;
	}
	
	public String getDatabaseUserPassword() {
		return databaseUserPassword;
	}
	
	public void setDatabaseUserPassword(String databaseUserPassword) {
		this.databaseUserPassword = databaseUserPassword;
	}
	
	public String getDatabaseServer() {
		return databaseServer;
	}
	
	public void setDatabaseServer(String databaseServer) {
		this.databaseServer = databaseServer;
	}
	
	public String getDatabaseManager() {
		return databaseManager;
	}
	
	public void setDatabaseManager(String databaseManager) {
		this.databaseManager = databaseManager;
	}
	
	public String getDatabasePortNumber() {
		return databasePortNumber;
	}
	
	public void setDatabasePortNumber(String databasePortNumber) {
		this.databasePortNumber = databasePortNumber;
	}
	
}
